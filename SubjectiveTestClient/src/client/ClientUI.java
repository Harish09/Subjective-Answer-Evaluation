    private void jButtonGetQuestionsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGetQuestionsActionPerformed
        // TODO add your handling code here:
        jTextFieldRollNo.setEditable(false);
        jPasswordField1.setEditable(false);
        int rno=0;
        try{
            rno = Integer.parseInt(jTextFieldRollNo.getText());
            if(jTextFieldRollNo.getText().equals("") || rno<0 || rno>150){
                throw new NumberFormatException(""+rno);
            }

        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Invalid Roll No.\n"+ex.getMessage());

            jTextFieldRollNo.setEditable(true);
            jPasswordField1.setEditable(true);
            return;
        }

        String passwordString;
        StringBuilder sb=new StringBuilder();
        sb.append(jPasswordField1.getPassword());
        passwordString = sb.toString();
        String matchingString = generatePassword(rno);

        if(!passwordString.equals(matchingString)){
            JOptionPane.showMessageDialog(this, "Password Mismatch!!");

            jTextFieldRollNo.setEditable(true);
            jPasswordField1.setEditable(true);
            return;
        }

        try {
            oos = new ObjectOutputStream(connectionManager.getWorkingSocket().getOutputStream());
            oos.writeUTF(jTextFieldRollNo.getText());
            oos.flush();

            ois = new ObjectInputStream(connectionManager.getWorkingSocket().getInputStream());
            testTime = ois.readInt();
            if(testTime==-1){
                JOptionPane.showMessageDialog(this, "Duplicate Roll No.!!\n\nA client has already connected using this roll no.\nCannot proceed, Exiting.\nPlease contact Administrator");
                System.exit(-1);
            }
            int noOfQuestions = ois.readInt();
            questionPaper = new String[noOfQuestions];
            answerPaper = new String[noOfQuestions];

            for(int i=0; i<questionPaper.length;i++){
                questionPaper[i]=ois.readUTF();
                answerPaper[i]="Ans. "+(i+1)+": ";
                jComboBoxQuestionNo.addItem(""+(i+1));
            }
            timer = new Timer(testTime, this);
            JOptionPane.showMessageDialog(this, "Instructions:\n\n* There are "+questionPaper.length+" questions.\n* Each question carries 10 grade points.\n* Answers are expected to be subjective\n* Total time for test is "+testTime+" minutes.\n* Click ok to start the test.\n\nAll The Best!!");
            timer.start();
            canClose=false;
            jButtonSubmitAllAnswers.setEnabled(true);

        } catch (IOException ex) {
            notifyNetworkFailureAndExit(ex);
        }
    }//GEN-LAST:event_jButtonGetQuestionsActionPerformed

