    protected void sendAnswers() {
        try {
            for(int i=0;i<answerPaper.length;i++){
                oos.writeUTF(answerPaper[i]);
            }
            oos.flush();
        } catch (IOException ex) {
            notifyNetworkFailureAndExit(ex);
        }
        JOptionPane.showMessageDialog(this, "All answers submitted. All the best for results! \n\nExiting...");
        System.exit(0);
    }

    protected void updateTimerDisplay(long min, long sec) {
        jLabelTimeDisplay.setText(""+min+" : "+sec);
    }

    private void notifyNetworkFailureAndExit(Exception exception) {
        JOptionPane.showMessageDialog(this, "NETWORK ERROR!!\n\n"+exception);
        System.exit(-1);
    }

