    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        // TODO add your handling code here:

        try{
            serverPort = Integer.parseInt(jTextFieldPortNo.getText());
            backlog = Integer.parseInt(jTextFieldBacklog.getText());
            driverName = jTextFieldDriverName.getText();
            url = jTextFieldURL.getText();
            dbName = jTextFieldDbName.getText();
            userName = jTextFieldUsername.getText();
            password = new String(jPasswordFieldPassword.getPassword());
        }catch(NumberFormatException nex){
            notifyInvalidAndClear();
            return;
        }
        caller.createUI(serverPort, backlog, driverName, url, dbName, userName, password);
    }//GEN-LAST:event_jButtonSubmitActionPerformed

